// Find Steam App ID by game title
function findAppId(gameTitle) {
    const normalizeTitle = (title) => {
        return title.toLowerCase()
            .replace(/[®™©]/g, '')
            // Only remove punctuation, keep letters (including Chinese/Unicode)
            .replace(/[^\p{L}\p{N}\s]/gu, ' ')
            .replace(/\s+/g, ' ')
            .trim();
    };
    
    const normalizedSearch = normalizeTitle(gameTitle);
    console.log(`Searching for: "${gameTitle}" → normalized: "${normalizedSearch}"`);
    
    // 1. Direct exact lookup
    if (appIdMapping[normalizedSearch]) {
        console.log(`Direct match found: ${appIdMapping[normalizedSearch]}`);
        return appIdMapping[normalizedSearch];
    }
    
    // 2. Try without spaces (compact match)
    const compactSearch = normalizedSearch.replace(/\s+/g, '');
    for (const [key, value] of Object.entries(appIdMapping)) {
        if (key.replace(/\s+/g, '') === compactSearch) {
            console.log(`Compact match found: "${key}" → ${value}`);
            return value;
        }
    }
    
    // 3. Check if any mapping key is an EXACT substring of the search
    // This helps when search is "古剑奇谭二 gujian2" and we have key "古剑奇谭二"
    for (const [key, value] of Object.entries(appIdMapping)) {
        // Check if the search starts with the key (exact word boundary)
        if (normalizedSearch.startsWith(key + ' ') || normalizedSearch === key) {
            console.log(`Starts-with match found: "${key}" → ${value}`);
            return value;
        }
    }
    
    // 4. Partial match as last resort - prefer LONGER/more specific matches
    let bestMatch = null;
    let bestMatchLength = 0;
    
    for (const [key, value] of Object.entries(appIdMapping)) {
        if (key.includes(normalizedSearch) || normalizedSearch.includes(key)) {
            // Prefer longer key matches (more specific)
            if (key.length > bestMatchLength) {
                bestMatch = value;
                bestMatchLength = key.length;
            }
        }
    }
    
    if (bestMatch) {
        console.log(`Partial match found (best): length ${bestMatchLength} → ${bestMatch}`);
    } else {
        console.log(`No match found`);
    }
    
    return bestMatch;
}

// Get Steam image URL - direct CDN, no API needed
function getSteamImageUrl(appId, size = 'small') {
    if (!appId) return null;
    
    const baseUrl = 'https://cdn.cloudflare.steamstatic.com/steam/apps';
    
    switch(size) {
        case 'small':
            return `${baseUrl}/${appId}/capsule_sm_120.jpg`;
        case 'header':
            return `${baseUrl}/${appId}/header.jpg`;
        default:
            return `${baseUrl}/${appId}/capsule_sm_120.jpg`;
    }
}

